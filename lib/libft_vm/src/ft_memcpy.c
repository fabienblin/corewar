#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t				i;
	unsigned char		*rep_dst;
	const unsigned char	*rep_src;

	i = 0;
	rep_dst = dst;
	rep_src = (const unsigned char *)src;
	if (n == 0)
		return (dst);
	while (i < n)
	{
		rep_dst[i] = rep_src[i];
		i++;
	}
	return (dst);
}

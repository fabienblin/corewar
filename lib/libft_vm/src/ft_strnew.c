#include "libft.h"
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*ad;

	i = 0;
	if ((ad = ft_memalloc(sizeof(*ad) * size + 1)) == NULL)
		return (NULL);
	while (i < size)
	{
		ad[i] = 0;
		i++;
	}
	ad[i] = '\0';
	return (ad);
}

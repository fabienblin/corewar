#include "libft.h"
#include <string.h>

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	*rep_dst;
	unsigned char	*rep_src;

	i = 0;
	rep_dst = (unsigned char *)dst;
	rep_src = (unsigned char *)src;
	while (i < n)
	{
		rep_dst[i] = rep_src[i];
		if (rep_src[i] == (unsigned char)c)
			return (rep_dst + i + 1);
		i++;
	}
	return (NULL);
}

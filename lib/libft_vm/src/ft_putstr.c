#include "libft.h"
#include <unistd.h>

void	ft_putstr(const char *s)
{
	unsigned int	i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
}

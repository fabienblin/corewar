#include "libft.h"
#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*rep_s;

	i = 0;
	rep_s = (unsigned char *)s;
	if (n == 0)
		return (NULL);
	while (i < n)
	{
		if (rep_s[i] == (unsigned char)c)
			return ((unsigned char *)rep_s + i);
		i++;
	}
	return (NULL);
}

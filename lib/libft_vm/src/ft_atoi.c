#include "libft.h"

long		ft_atoi(const char *str)
{
	long	nb;
	int		neg;

	nb = 0;
	neg = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n'
			|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			neg = 1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = (nb * 10) + (*str - '0');
		str++;
	}
	if (neg)
		nb = -nb;
	return (nb);
}

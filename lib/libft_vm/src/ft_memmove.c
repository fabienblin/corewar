#include "libft.h"
#include <stdlib.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;
	char	*d;
	char	*s;
	char	*tmp;

	i = 0;
	s = (char *)src;
	d = (char *)dst;
	if ((tmp = (char *)malloc(sizeof(char) * len)) == NULL)
		return (dst);
	while (i < len)
	{
		tmp[i] = s[i];
		i++;
	}
	i = 0;
	while (i < len)
	{
		d[i] = tmp[i];
		i++;
	}
	free(tmp);
	return (dst);
}

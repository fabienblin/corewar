#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*rep;

	i = ft_strlen(s1);
	if ((rep = (char *)malloc(sizeof(*rep) * i + 1)) == NULL)
		return (NULL);
	rep = ft_strcpy(rep, s1);
	return (rep);
}

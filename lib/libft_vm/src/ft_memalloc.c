#include "libft.h"
#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	size_t			i;
	unsigned char	*ad;

	i = 0;
	if ((ad = (void *)malloc(sizeof(*ad) * size)) == NULL)
		return (NULL);
	while (i < size)
	{
		ad[i] = 0;
		i++;
	}
	return (ad);
}

#include "libft.h"
#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*rep;

	i = 0;
	rep = b;
	if (len == 0)
		return (b);
	while (i < len)
	{
		rep[i] = (unsigned char)c;
		i++;
	}
	return (b);
}

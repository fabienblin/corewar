#include "libft.h"
#include <string.h>

size_t	ft_lstlen(t_list *list)
{
	size_t	nb;
	t_list	*lst;

	lst = list;
	nb = 0;
	if (lst == NULL)
		return (0);
	while (lst->next != NULL)
	{
		lst = lst->next;
		nb++;
	}
	return (nb);
}
